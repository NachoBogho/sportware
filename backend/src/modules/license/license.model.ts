import { Schema, model } from 'mongoose';

const licenseSchema = new Schema({
  key: {
    type: String,
    required: true,
    unique: true,
  },
  isActive: {
    type: Boolean,
    default: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  expiresAt: {
    type: Date,
    required: true,
  },
});

const License = model('License', licenseSchema);

export default License;